SELECT OP.VENDOR_PARTY_ID as 'Client',
       SRS.ORIGIN_FACILITY_ID as 'Facility',
       PA2.POSTAL_CODE AS 'Facility Zip',
       S.PACKED_DATE as 'Date',
       PA.TO_NAME as 'Customer Name',
       OH.DISPLAY_ID as 'Order Number',
       OH.EXTERNAL_ID as 'External Id',
       SPRS.ESTIMATED_AMOUNT as 'Cost',
       SPRS.TRACKING_CODE as 'Tracking Code',
       SRS.CARRIER_PARTY_ID as 'Carrier',
       SRS.SHIPMENT_METHOD_ENUM_ID as 'Method',
       PA.POSTAL_CODE as 'Destination Zip',
       SBT.DESCRIPTION as 'Package Type',
       SP.WEIGHT as 'Package Weight',
       CONCAT(UOM.DESCRIPTION,' (',UOM.ABBREVIATION,')') as 'Package Weight Uom'
FROM SHIPMENT_ROUTE_SEGMENT SRS
INNER JOIN SHIPMENT S ON S.SHIPMENT_ID = SRS.SHIPMENT_ID
INNER JOIN SHIPMENT_ITEM_SOURCE SIS ON SIS.SHIPMENT_ID = SRS.SHIPMENT_ID
INNER JOIN ORDER_ITEM OI ON OI.ORDER_ID = SIS.ORDER_ID AND OI.PRODUCT_ID = SIS.PRODUCT_ID
INNER JOIN ORDER_PART OP ON OP.ORDER_ID = OI.ORDER_ID AND OP.ORDER_PART_SEQ_ID = OI.ORDER_PART_SEQ_ID
INNER JOIN ORDER_HEADER OH ON OH.ORDER_ID = OP.ORDER_ID
INNER JOIN SHIPMENT_PACKAGE_ROUTE_SEG SPRS ON SPRS.SHIPMENT_ID = SRS.SHIPMENT_ID AND SPRS.SHIPMENT_ROUTE_SEGMENT_SEQ_ID = SRS.SHIPMENT_ROUTE_SEGMENT_SEQ_ID
INNER JOIN POSTAL_ADDRESS PA ON PA.CONTACT_MECH_ID = SRS.DEST_POSTAL_CONTACT_MECH_ID
INNER JOIN SHIPMENT_PACKAGE SP ON SP.SHIPMENT_ID = SPRS.SHIPMENT_ID AND SP.SHIPMENT_PACKAGE_SEQ_ID = SPRS.SHIPMENT_PACKAGE_SEQ_ID
INNER JOIN SHIPMENT_BOX_TYPE SBT ON SBT.SHIPMENT_BOX_TYPE_ID = SP.SHIPMENT_BOX_TYPE_ID
INNER JOIN UOM ON UOM.UOM_ID = SP.WEIGHT_UOM_ID
INNER JOIN FACILITY_CONTACT_MECH FCM ON FCM.FACILITY_ID = SRS.ORIGIN_FACILITY_ID AND FCM.THRU_DATE IS NULL AND FCM.CONTACT_MECH_PURPOSE_ID = 'PostalPrimary'
INNER JOIN POSTAL_ADDRESS PA2 ON PA2.CONTACT_MECH_ID = FCM.CONTACT_MECH_ID
WHERE SRS.ORIGIN_FACILITY_ID = 'BOL_DC_FONTANA_2'
AND S.PACKED_DATE BETWEEN '2023-01-01 00:00' AND '2023-06-29 00:00'
GROUP BY OP.VENDOR_PARTY_ID, SRS.ORIGIN_FACILITY_ID, PA2.POSTAL_CODE, S.PACKED_DATE, PA.TO_NAME, OH.DISPLAY_ID, OH.EXTERNAL_ID, SPRS.ESTIMATED_AMOUNT, SPRS.TRACKING_CODE, SRS.CARRIER_PARTY_ID, SRS.SHIPMENT_METHOD_ENUM_ID, PA.POSTAL_CODE, SBT.DESCRIPTION, SP.WEIGHT
ORDER BY S.PACKED_DATE DESC;


