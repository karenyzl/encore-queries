SELECT P.PSEUDO_ID AS 'SKU',
       PI.ID_VALUE AS 'UPCA',
       P.PRODUCT_NAME AS 'NAME',
       A.FACILITY_ID,
       SUM(CASE WHEN A.QUANTITY_ON_HAND_TOTAL > 0 THEN A.QUANTITY_ON_HAND_TOTAL ELSE 0 END) AS 'CURRENT',
       SUM(CASE
           WHEN DATEDIFF(CURDATE(),DATE(A.RECEIVED_DATE)) < 30
               THEN A.QUANTITY_ON_HAND_TOTAL
           ELSE 0 END) AS '0-30 DAYS',
       SUM(CASE
           WHEN DATEDIFF(CURDATE(),DATE(A.RECEIVED_DATE)) BETWEEN 31 AND 60
               THEN A.QUANTITY_ON_HAND_TOTAL
           ELSE 0 END) AS '31-60 DAYS',
       SUM(CASE
           WHEN DATEDIFF(CURDATE(),DATE(A.RECEIVED_DATE)) BETWEEN 61 AND 90
               THEN A.QUANTITY_ON_HAND_TOTAL
           ELSE 0 END) AS '61-90 DAYS',
       SUM(CASE
           WHEN DATEDIFF(CURDATE(),DATE(A.RECEIVED_DATE)) BETWEEN 91 AND 120
               THEN A.QUANTITY_ON_HAND_TOTAL
           ELSE 0 END) AS '91-120 DAYS',
       SUM(CASE
           WHEN DATEDIFF(CURDATE(),DATE(A.RECEIVED_DATE)) BETWEEN 121 AND 180
               THEN A.QUANTITY_ON_HAND_TOTAL
           ELSE 0 END) AS '121-180 DAYS',
       SUM(CASE
           WHEN DATEDIFF(CURDATE(),DATE(A.RECEIVED_DATE)) BETWEEN 181 AND 365
               THEN A.QUANTITY_ON_HAND_TOTAL
           ELSE 0 END) AS '181-365 DAYS',
       SUM(CASE
           WHEN DATEDIFF(CURDATE(),DATE(A.RECEIVED_DATE)) > 365
               THEN A.QUANTITY_ON_HAND_TOTAL
           ELSE 0 END) AS '> 365 DAYS'
FROM PRODUCT P
INNER JOIN ASSET A ON A.PRODUCT_ID = P.PRODUCT_ID
INNER JOIN PRODUCT_IDENTIFICATION PI ON PI.PRODUCT_ID = P.PRODUCT_ID AND PI.PRODUCT_ID_TYPE_ENUM_ID = 'PIDTUPCA'
WHERE P.OWNER_PARTY_ID = 'KCNY'
GROUP BY P.PSEUDO_ID, PI.ID_VALUE, P.PRODUCT_NAME, A.FACILITY_ID;



